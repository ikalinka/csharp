using System;
namespace csharpcore
{
	public class MathFunc
	{
		public int Multiply(int a, int b)
		{
			return a * b;
		}

		public double Divide(int a, int b)
		{
			return (double) a / b;
		}

		public int Addition(int a, int b)
		{
			return a + b;
		}

		public int Substraction(int a, int b)
		{
			return a - b;
		}
	}
}
